      SUBROUTINE CDATE (CARRAY)
C
C     RETURNS THE CURRENT SYSTEM DATE IN A 9 CHARACTER
C     MILITARY STYLE FORMAT.  E.G. 07 APR 82
C
      CHARACTER CARRAY*(*)
C
      ILEN = LEN(CARRAY)
      IF (ILEN.LT.9) THEN
      CALL CHRFIL (CARRAY, '*')
      RETURN
      ENDIF
C
      CALL SYSTIM (JUL, ISEC)
      CARRAY = ' '
      CALL JULDAT (JUL, 117, CARRAY, NDATE)
C
      RETURN
      END
